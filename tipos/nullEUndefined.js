// Null e undefined representam ausência de valor

/*
 - Undefined é um tipo primitivo do javascript.
 - É atribuído a uma variável quando ela é declarada, mas ainda não recebeu um valor.
 - Também é retornado automaticamente por JavaScript quando uma variável é declarada, mas não inicializada
 - Indica uma falta de valor atribuído explicitamente.
 */

let valorUndefined;
console.log(valorUndefined)

valorUndefined = undefined
console.log(valorUndefined)

/*
  null é um valor especial em JavaScript que indica a ausência intencional de valor.
*/
const nullVar = null
console.log(nullVar)